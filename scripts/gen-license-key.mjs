/**
 * Одноразовая генерация ключей для офлайн-лицензий Margli Pro.
 *
 * Запуск:  node scripts/gen-license-key.mjs
 *
 * Создаёт пару ECDSA P-256:
 *   - ПУБЛИЧНЫЙ ключ → src/lib/license-pubkey.ts (вшивается в расширение,
 *     безопасно коммитить — им нельзя подделать код, только проверить);
 *   - ПРИВАТНЫЙ ключ → scripts/license-keys/private.jwk (в .gitignore,
 *     НИКОГДА не коммитить и не класть в расширение; им подписываются коды).
 *
 * Безопасность: расширение умеет ТОЛЬКО проверять подпись (публичный ключ).
 * Сгенерировать валидный Pro-код можно лишь приватным ключом, который есть
 * только у владельца. Украсть из расширения нечего: там нет секрета.
 *
 * ВАЖНО: для боевого использования сгенерируй ключ САМ на своей машине и
 * храни private.jwk в надёжном месте (менеджер паролей). Если ключ утёк —
 * перегенерируй (старые коды перестанут проверяться) и перевыпусти расширение.
 */
import { webcrypto as crypto } from "node:crypto";
import { writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const kp = await crypto.subtle.generateKey(
  { name: "ECDSA", namedCurve: "P-256" },
  true,
  ["sign", "verify"],
);

const pubJwk = await crypto.subtle.exportKey("jwk", kp.publicKey);
const privJwk = await crypto.subtle.exportKey("jwk", kp.privateKey);

// Только нужные поля публичного ключа (kty/crv/x/y).
const pub = { kty: pubJwk.kty, crv: pubJwk.crv, x: pubJwk.x, y: pubJwk.y };

mkdirSync(join(root, "scripts", "license-keys"), { recursive: true });
writeFileSync(
  join(root, "scripts", "license-keys", "private.jwk"),
  JSON.stringify(privJwk),
);
writeFileSync(
  join(root, "scripts", "license-keys", "public.jwk"),
  JSON.stringify(pub),
);

const out =
  "// AUTO-GENERATED by scripts/gen-license-key.mjs — НЕ редактировать руками.\n" +
  "// Только ПУБЛИЧНЫЙ ключ. Безопасно коммитить: им нельзя подделать код.\n" +
  "export const LICENSE_PUBLIC_KEY_JWK = " +
  JSON.stringify(pub) +
  " as const;\n";
writeFileSync(join(root, "src", "lib", "license-pubkey.ts"), out);

console.log("OK. Public key → src/lib/license-pubkey.ts");
console.log("Private key → scripts/license-keys/private.jwk (gitignored, храни безопасно)");
